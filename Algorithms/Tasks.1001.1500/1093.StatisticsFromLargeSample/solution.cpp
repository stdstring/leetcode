#include <algorithm>
#include <vector>

#include "gtest/gtest.h"

namespace
{

class Solution
{
public:
    [[nodiscard]] std::vector<double> sampleStats(std::vector<int> const &count) const
    {
        int min = INT_MAX;
        int max = INT_MIN;
        size_t totalCount = 0;
        uint64_t sum = 0;
        int mode = 0;
        for (int number = 0; number < static_cast<int>(count.size()); ++number)
        {
            if (count[number] > 0)
            {
                min = std::min(min, number);
                max = std::max(max, number);
                totalCount += count[number];
                sum += (static_cast<uint64_t>(number) * count[number]);
                mode = count[mode] >= count[number] ? mode : number;
            }
        }
        // calc median
        int medianLeft = 0;
        const size_t medianCount = (totalCount / 2) + (totalCount % 2);
        size_t partCount = 0;
        for (;partCount < medianCount; partCount += count[medianLeft++]);
        --medianLeft;
        int medianRight = medianLeft;
        if ((totalCount % 2 == 0) && (partCount == medianCount))
        {
            ++medianRight;
            for (;count[medianRight] == 0; ++medianRight);
        }
        return {1.0 * min, 1.0 * max, (1.0 * sum) / totalCount, (1.0 * medianLeft + 1.0 * medianRight) / 2, 1.0 * mode};
    }
};

}

namespace
{

void checkSampleStats(std::vector<double> const &expected, std::vector<int> const &count)
{
    const Solution solution;
    const std::vector<double> actual(solution.sampleStats(count));
    ASSERT_EQ(expected.size(), actual.size());
    for (size_t index = 0; index < expected.size(); ++index)
        ASSERT_NEAR(expected[index], actual[index], 0.00001);
}

}

